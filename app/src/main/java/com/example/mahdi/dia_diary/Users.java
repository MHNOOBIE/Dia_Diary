package com.example.mahdi.dia_diary;

public class Users {
    String name;
    String country;

    public Users() {
    }

    public String getName() {
        return name;
    }

    public String getCountry() {
        return country;
    }

    public Users(String name, String country) {
        this.name = name;
        this.country = country;
    }
}
